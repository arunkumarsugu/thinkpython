'''
Assume that we execute the following assignment statements:
width = 17
height = 12.0
delimiter = '.'
For each of the following expressions, write the value of the expression and the type (of the value of
the expression).
1. width/2
2. width/2.0
3. height/3
4. 1 + 2 * 5
5. delimiter * 5
'''
width = 17
height = 12.0
delimiter = '.'

a = width/2
print(a)
print(type(a))
b = width/2.0
print(b)
print(type(b))
c = height/3
print(c)
print(type(c))
d = 1+2*5
print(d)
print(type(d))
e = delimiter*5
print(e)
print(type(e))